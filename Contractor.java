class Contractor{
	static void contract() {
    System.out.println("Let's negotiate the terms of the contract.");
  }
  
  static void scheduleWork(String startDate, String endDate) {
    System.out.println("The project will start on " + startDate + " and end on " + endDate);
  }
  
  static void assignTeam(String teamMembersname) {
    System.out.println("The project team includes: " + teamMembersname);
  }
  
  static void acquireMaterials(String material) {
    System.out.println("The required materials are: " + material);
  }
  
  static void manageBudget(float budget) {
    System.out.println("The project budget is: $" + budget);
  }
  
  static void superviseWork() {
    System.out.println("I will be supervising the work progress.");
  }
  
  static void handleIssues() {
    System.out.println("I will handle any issues or problems that arise during the project.");
  }
  
  static void ensureCompliance() {
    System.out.println("We will ensure that all work meets the necessary regulations and standards.");
  }
  
  static void conductQualityChecks() {
    System.out.println("Regular quality checks will be conducted throughout the project.");
  }
   static void handoverProject() {
    System.out.println("Once the project is finished, it will be handed over to the client.");
  }
}